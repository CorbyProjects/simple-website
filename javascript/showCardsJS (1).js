document.getElementById("info1").addEventListener('click',function (event) {
		event.preventDefault();
		var card = document.getElementById("infoCard1");
		if (card.style.display == "none") {
			card.style.display = "block";
		}
		else {
			card.style.display = "none";
		}
		return false;
	},false);
